// Ternary operator

const active = true;

// normal way:

console.log('Ternary Operator (normal way):');
const mensaje = active ? 'Active' : 'Inactive';
console.log(mensaje);

// only if ... not else

console.log('Ternary Operator (only if ... not else):');
const mensaje2 = active && 'Active';
console.log(mensaje2);